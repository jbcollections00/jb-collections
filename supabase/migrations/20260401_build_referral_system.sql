create table if not exists public.coin_history (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  event_type text not null,
  amount integer not null default 0,
  description text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists coin_history_user_id_idx
  on public.coin_history (user_id);

create index if not exists coin_history_created_at_idx
  on public.coin_history (created_at desc);

create table if not exists public.referral_events (
  id bigint generated by default as identity primary key,
  inviter_id uuid references public.profiles(id) on delete set null,
  invited_user_id uuid references public.profiles(id) on delete set null,
  referral_code text,
  referral_valid boolean not null default false,
  inviter_reward integer not null default 0,
  invited_reward integer not null default 0,
  created_at timestamptz not null default now()
