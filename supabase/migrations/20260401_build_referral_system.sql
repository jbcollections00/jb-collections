do $$
begin
  if not exists (
    select 1
    from pg_policies
    where schemaname = 'public'
      and tablename = 'coin_history'
      and policyname = 'coin_history_select_own'
  ) then
    create policy coin_history_select_own
      on public.coin_history
      for select
      to authenticated
      using (auth.uid() = user_id);
  end if;
end $$;

do $$
begin
  if not exists (
    select 1
    from pg_policies
    where schemaname = 'public'
      and tablename = 'referral_events'
      and policyname = 'referral_events_select_own'
  ) then
    create policy referral_events_select_own
      on public.referral_events
      for select
      to authenticated
      using (
        auth.uid() = inviter_id
        or auth.uid() = invited_user_id
      );
  end if;
end $$;